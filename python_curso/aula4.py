# Tipos int e float
# int --> Numero inteiro ( O tipo int representa qualquer numero positivo ou negativo, inteiro sem sinal e considerado positivo)
# Exemplo : -2 , -7, 5, 14, 300, 1500 ...
print()
print(10)
print(-11)
print(0)

print()

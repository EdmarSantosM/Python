# O Tipo float representa qualuqer numero positivo ou negativo com casas decimais
# Observacao na programação  utiliza-se o ('.') sinal de ponto como separador de casas decimais
# Exemplo : 1.1 ,  10.50
print(50.3)
print(1.9)

print()

